const sortEndorsementsByLastName = endorsements => {
  const compare = (a, b) => {
    const aName = a.split(" ")
    const bName = b.split(" ")

    let aLastName
    let bLastName

    // Handle edge cases with aLastName
    if (aName[aName.length - 1] === "III") {
      aLastName = aName[aName.length - 2]
    } else if (aName[aName.length - 1] === "Jr.") {
      aLastName = aName[aName.length - 2]
    } else {
      aLastName = aName[aName.length - 1]
    }

    // Handle edge cases with bLastName suffixes
    if (bName[bName.length - 1] === "III") {
      bLastName = bName[bName.length - 2]
    } else if (bName[bName.length - 1] === "Jr.") {
      bLastName = bName[bName.length - 2]
    } else if (
      bName[bName.length - 2] === "Van" ||
      bName[bName.length - 2] === "La" ||
      bName[bName.length - 2] === "Te"
    ) {
      bLastName = bName[bName.length - 2]
    } else {
      bLastName = bName[bName.length - 1]
    }

    // compare the names and return either
    // a negative number, positive number
    // or zero.
    if (aLastName < bLastName) return -1
    if (aLastName > bLastName) return 1
    return 0
  }
  return endorsements.sort(compare)
}

const distributeEndorsementsEvenly = sortedEndorsements => {
  const numberOfColumns = 3
  const result = [[], [], []]

  const namesPerList = Math.ceil(sortedEndorsements.length / numberOfColumns)

  for (let line = 0; line < numberOfColumns; line++) {
    for (let i = 0; i < namesPerList; i++) {
      const value = sortedEndorsements[i + line * namesPerList]
      if (!value) continue //avoid adding "undefined" values
      result[line].push(value)
    }
  }
  return result
}

